package com.huawei.esdk.ec.domain.model.multiapponline;

import java.util.List;

public class Group
{
    private String origin;
    
    private String groupID;
    
    private String name;
    
    private String capacity;
    
    private String owner;
    
    private String joinFlag;
    
    private String manifesto;
    
    private String desc;
    
    private String groupType;
    
    private List<String> inviteList;
    
    private String fixDiscuss;
    
    private String senderAppID;
    
    private String senderType;
    
    public String getOrigin()
    {
        return origin;
    }
    
    public void setOrigin(String origin)
    {
        this.origin = origin;
    }
    
    public String getGroupID()
    {
        return groupID;
    }
    
    public void setGroupID(String groupID)
    {
        this.groupID = groupID;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getCapacity()
    {
        return capacity;
    }
    
    public void setCapacity(String capacity)
    {
        this.capacity = capacity;
    }
    
    public String getOwner()
    {
        return owner;
    }
    
    public void setOwner(String owner)
    {
        this.owner = owner;
    }
    
    public String getJoinFlag()
    {
        return joinFlag;
    }
    
    public void setJoinFlag(String joinFlag)
    {
        this.joinFlag = joinFlag;
    }
    
    public String getManifesto()
    {
        return manifesto;
    }
    
    public void setManifesto(String manifesto)
    {
        this.manifesto = manifesto;
    }
    
    public String getDesc()
    {
        return desc;
    }
    
    public void setDesc(String desc)
    {
        this.desc = desc;
    }
    
    public String getGroupType()
    {
        return groupType;
    }
    
    public void setGroupType(String groupType)
    {
        this.groupType = groupType;
    }
    
    public List<String> getInviteList()
    {
        return inviteList;
    }
    
    public void setInviteList(List<String> inviteList)
    {
        this.inviteList = inviteList;
    }
    
    public String getFixDiscuss()
    {
        return fixDiscuss;
    }
    
    public void setFixDiscuss(String fixDiscuss)
    {
        this.fixDiscuss = fixDiscuss;
    }
    
    public String getSenderAppID()
    {
        return senderAppID;
    }
    
    public void setSenderAppID(String senderAppID)
    {
        this.senderAppID = senderAppID;
    }
    
    public String getSenderType()
    {
        return senderType;
    }
    
    public void setSenderType(String senderType)
    {
        this.senderType = senderType;
    }
}
